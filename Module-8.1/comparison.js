/**
 * 1. Greater Than: >
 * 2. Less Than: <
 * 3. equal to: ==, ===
 * 4. Greater than or equal to: >=
 * 5. Less than or equal to: <=
 * 6. Not equal to: !=, !==
 * 7. And: &&
 * 8. Or: ||
 */

const a = 10;
const b = 7;
console.log(a > b);
console.log(a < b);
console.log(a === b);
console.log(a !== b);